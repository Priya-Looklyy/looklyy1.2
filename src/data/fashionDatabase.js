// Helper functions for database operations
export const getAllSliders = () => {
  return Object.values(fashionDatabase)
}

export const getSliderById = (id) => {
  return Object.values(fashionDatabase).find(slider => slider.id === id)
}

export const getSliderImages = (sliderId) => {
  const slider = getSliderById(sliderId)
  return slider ? slider.images : []
}

export const getTotalImageCount = () => {
  return Object.values(fashionDatabase).reduce((total, slider) => total + slider.images.length, 0)
}

// Ready for image upload functionality
export const updateSliderImages = (sliderId, newImages) => {
  const sliderKey = `slider${sliderId}`
  if (fashionDatabase[sliderKey]) {
    fashionDatabase[sliderKey].images = newImages
    return true
  }
  return false
}
